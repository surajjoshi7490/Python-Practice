# Suggest an activity based on the weather 
# (e.g., Sunny - Go for a walk, Rainy - Read a book, Snowy - Build a snowman).

weather=str(input("What is the weather there : \n Sunny , Rainy , Winter"))
if weather=="sunny":
    print("Go for a walk")
elif weather=="rainy":
    print("Read a book")
elif weather=="winter":
    print("enjoy your day with pakodas ")